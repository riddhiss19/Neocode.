def calculate_volume(length, width, height):
    """
    Calculates the volume of a rectangular prism given its length, width, and height.

    Args:
        length (float): The length of the rectangular prism.
        width (float): The width of the rectangular prism.
        height (float): The height of the rectangular prism.

    Returns:
        float: The volume of the rectangular prism.
    """
    volume = length * width * height
    return volume
